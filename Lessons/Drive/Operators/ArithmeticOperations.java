package Lessons.Drive.Operators;

public class ArithmeticOperations {
    
        public static void main(String[] args) {
            // declare variables
            int a = 10;
            int b = 20;

            // addition
            System.out.println("a + b = " + (a + b));

            // subtraction
            System.out.println("a - b = " + (a - b));

            // multiplication
            System.out.println("a * b = " + (a * b));

            // Division
            System.out.println("a / b = " + (a / b));

            // modulo
            System.out.println("a % b = " + (a % b));
        }
    }
